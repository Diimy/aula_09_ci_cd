def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def sums(val1,val2):
    return val1+val2

def sub(val1,val2):
    return val1-val2

def divs(val1,val2):
    return val1/val2

def multp(val1,val2):
    return val1*val2